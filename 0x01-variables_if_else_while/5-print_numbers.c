#include <stdio.h>

/**                                                                                                                                                                                        
 * main - Prints the alphabet in lowercase.                                                                                                                                                
 *                                                                                                                                                                                         
 * Return: Always 0.                                                                                                                                                                       
 */
int main(void)
{
        int letter;

        for (letter = 0; letter <= 9; letter++)
                printf(letter);

        return (0);
}
